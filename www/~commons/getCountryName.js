function getCountryName(countryCode, lang) {
    if(!countryCode){
        console.log("error: not country code given");
        return false;
    }
    if (!lang) {
        lang = "en";
    }

    if (isoCountries.hasOwnProperty(countryCode.toUpperCase())) {
        var country = isoCountries[countryCode.toUpperCase()];
        if (country.hasOwnProperty(lang)) {
            return country[lang];
        } else if ("en" != lang) {
            if (country.hasOwnProperty("en")) {
                return country["en"];
            }
        }

    }
    return countryCode;
}